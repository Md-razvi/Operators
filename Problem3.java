package Operators;

public class Problem3 {
    public static void main(String[] args)
    {
        int a=23,b=54;
        //Single = means assignments operator
        //Double == means equal to operator  
        // One exclamation and single = means not equal to i.e(!=)
        System.out.println(a==b);// prints boolean value false
        System.out.println(a!=b);// prints boolean value True
        System.out.println(a==23);// prints boolean value True

    }
    
}
